/**
 * Title: Comparison Operators
 * Description: < > >= <= == === != !==
 */

let price = 200;

console.log(price > 300);
console.log(price >= 199);
console.log(price < 199);
console.log(price <= 200);
console.log(price == 200);
console.log(price === '200');
console.log(price == '200');
console.log(price !== '200');
console.log(price !== 200);
console.log(price != 200);
