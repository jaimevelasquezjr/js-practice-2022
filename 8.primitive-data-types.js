/**
 * Title: Basics of Primitive Data Types (string, undefined, boolean, number)
 */

let name = 'jaime';
console.log(typeof name); // Result: String

let age = 28;
console.log(typeof age); // Result: Number

let isMaried = false;
console.log(typeof isMaried); // Result: Boolean

let colors;
console.log(typeof colors); // Result: Undefined

let isNull = null;
console.log(typeof isNull); // Result: Object

let isBigInt = 1234567890123456789012345678901234567890n;
console.log(typeof isBigInt); // Result: bigint
